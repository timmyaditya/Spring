package org.timmyaditya.numberplay.dto;

public class ModelDto {
	private int enteredNumber;
	private int digitSum;
	private int digitReverse;
	public int getEnteredNumber() {
		return enteredNumber;
	}
	public void setEnteredNumber(int enteredNumber) {
		this.enteredNumber = enteredNumber;
	}
	public int getDigitSum() {
		return digitSum;
	}
	public void setDigitSum(int digitSum) {
		this.digitSum = digitSum;
	}
	public int getDigitReverse() {
		return digitReverse;
	}
	public void setDigitReverse(int digitReverse) {
		this.digitReverse = digitReverse;
	}
	

}
